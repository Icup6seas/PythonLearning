
# if, elif, else statements
zoo = ['Tiger', 'Bear', 'Wolf', 'Kitty']

for animal in zoo:
    if animal == 'Bear':
        print(f"Shit! It's a {animal}!")
    elif animal == 'Tiger':
        print(f"{animal}s - AKA - Danger Kittens")
    elif animal == 'Wolf':
        print(f"{animal}'s are related to Geralt?!")
    else:
        print(f"{animal}s are miniature Tigers")


